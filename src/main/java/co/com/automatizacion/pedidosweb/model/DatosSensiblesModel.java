package co.com.automatizacion.pedidosweb.model;

public class DatosSensiblesModel {

    private String cedulaai;
    private String contrasena;
    private String titulomodal;

    public String getCedulaai() {
        return cedulaai;
    }

    public void setCedulaai(String cedulaai) {
        this.cedulaai = cedulaai;
    }

    public String getContrasena() {
        return contrasena;
    }

    public void setContrasena(String contrasena) {
        this.contrasena = contrasena;
    }

    public String getTitulomodal() {
        return titulomodal;
    }

    public void setTitulomodal(String titulomodal) {
        this.titulomodal = titulomodal;
    }
}
